void matxvec(float A[1024], float B[1024][64], float C[64])
{
 int j;
 float a[1024];
 float c[64];
#pragma HLS ARRAY_PARTITION variable = c block factor = 16 dim = 1

 memcpy(a, (float*)A, 1024 * sizeof(float));

 for (j = 0; j < 64; j++)
 {
#pragma HLS UNROLL
 c[j] = 0;
 }

    for (int k = 0; k < 1024; k++)
    {
        for (j = 0; j < 64; j++)
        {
#pragma HLS UNROLL
#pragma HLS PIPELINE
 c[j] += a[k] * B[k][j];
        }
    }

 memcpy((float*)C, (float*)c, 64 * sizeof(float));

}

